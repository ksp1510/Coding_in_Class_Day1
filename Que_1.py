# Write a Python program which accepts the radius of a circle from the user and compute the area.
print("Welcome to the program that takes radius from user and calculate area of the circle\n")
radius = float(input("Enter the radius of the circle in cm: "))

area = (radius**2)*3.14

print(f"\nThe radius of the circle is '{radius} cm'\nThe area of the circle is '{area} sq cm'")
print("\nThank you for using the program")